class KthLargest {
private:
    priority_queue<int, vector<int>, greater<>> heap;
    int sz;
public:
    KthLargest(int k, const vector<int> &nums) : sz(k) {
        for (const int num : nums) {
            heap.push(num);
        }
    }

    int add(int val) {
        heap.push(val);
        while (heap.size() > sz) {
            heap.pop();
        }
        return heap.top();
    }
};

