class SeatManager {
private:
    priority_queue<int, vector<int>, greater<>> heap;
public:
    SeatManager(int n) {
        for (int i = 1; i <= n; ++i) {
            heap.push(i);
        }
    }

    int reserve() {
        auto t = heap.top();
        heap.pop();
        return t;
    }

    void unreserve(int seatNumber) {
        heap.push(seatNumber);
    }
};

